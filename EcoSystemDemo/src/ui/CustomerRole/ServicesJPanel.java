    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) serviceJTable.getModel();
        model.setRowCount(0);
        for(Network n: ecosystem.getNetworkList()){
            for(Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
                if(e.getEnterpriseType().toString().equals("Service")){
                    if(e.getZipcode() == customer.getZipcode()){
                        System.out.println("Enterprise Name :" + e.getName());
                            for(Organization o : e.getOrganizationDirectory().getOrganizationList()){
                                if(o.getType().equals("Cleaning") || o.getType().equals("Mechanic") || o.getType().equals("Electrician") || o.getType().equals("Plumbing")){
                                    Object[] row = new Object[7];
                                    row[0] = e.getName();
                                    row[1] = o.getType();
                                    row[2] = o.getName();
                                    row[3] = e.getPhone();
                                    row[4] = e.getStreetaddress();
                                    row[5] = e.getCity();
                                    row[6] = e.getZipcode();
                                    model.addRow(row);
                                }
                            }                        
                    }
                }
            }
        }
    }
    
