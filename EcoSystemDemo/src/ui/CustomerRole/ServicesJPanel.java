    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        // TODO add your handling code here:
        int selectedrow = serviceJTable.getSelectedRow();
        if(selectedrow<0){
            JOptionPane.showMessageDialog(null,"Select a row","Warning",JOptionPane.WARNING_MESSAGE);
        }
        
        String name = (String) serviceJTable.getValueAt(selectedrow, 0);
        for(Network n : ecosystem.getNetworkList()){
            for(Enterprise e: n.getEnterpriseDirectory().getEnterpriseList()){
                if(e.getName().equals(name)){
                    enterprise = e;
                }
            }
        }
        
        String orgname = (String) serviceJTable.getValueAt(selectedrow, 2);
        System.out.println("input :" + orgname);
        for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
            if(o.getName().equals(orgname)){
                this.organization = o;
                System.out.println("Organization Name : " + organization.getName());
                System.out.println("Organization Type : " + organization.getType());
            }
        }
        
        workrequest = new ServiceWorkRequest(organization.getType(), "Assigned", organization.getName(), organization.getPhone(), organization.getEmail(), organization.getOrganizationID(), customer);
        workrequest.setMessage(tfMessage.getText());
        if(enterprise.getRequestList() != null){
            enterprise.getRequestList().add((ServiceWorkRequest) workrequest);
        }
        else{
            enterprise.newRequestList();
            enterprise.getRequestList().add((ServiceWorkRequest) workrequest);
        }

        Date date = new Date();
        workrequest.setRequestDate(date);
            
        
        System.out.println("Work Request ID : " + workrequest.getRequestID());
        System.out.println("Customer Name : " + workrequest.getCustomer().getName());

    }//GEN-LAST:event_btnBookActionPerformed

