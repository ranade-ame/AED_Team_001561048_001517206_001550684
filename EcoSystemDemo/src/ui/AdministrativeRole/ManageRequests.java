    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) requestJTable.getModel();
        model.setRowCount(0);
        Object[] row = new Object[7];
        for(ServiceWorkRequest sw : enterprise.getRequestList()){
            
            row[0] = sw.getRequestID();
            row[1] = sw.getType();
            row[2] = sw.getCustomer().getName();
            row[3] = sw.getCustomer().getStreetAddress();
            row[4] = sw.getRequestDate();
            row[5] = sw.getStatus();
            row[6] = sw.getMessage();
            model.addRow(row);
        }
        
    }
